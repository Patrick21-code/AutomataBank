/*
    DFA is defined as a 5-tuple: (Q, Σ, δ, q0, F)
    - Q = set of states
    - Σ = alphabet (input symbols)
    - δ = transition function
    - q0 = start state
    - F = set of accept states
*/

//define states (Q)
const STATES = {
    S0: 'idle',             //q0 - start state
    S1: 'account_entry',   
    S2: 'pin_entry',
    S3: 'authenticated',
    S4: 'amount_entry',
    S5: 'balance_display',
    S6: 'rejected',         //wrong credentials, can retry
    S7: 'done'              //accepting state
}

//define alphabet (Σ)
//all possible inputs that my DFA can receive
const INPUTS = {
    START: 'start',
    ENTER_DIGIT: 'enter_digit',
    SUBMIT_ACCOUNT: 'submit_account',
    CORRECT_ACCOUNT: 'correct_account', //internal input
    //passed
    WRONG_ACCOUNT: 'wrong_account',     //internal input
    //failed
    SUBMIT_PIN: 'submit_pin',           
    CORRECT_PIN: 'correct_pin',         //internal input (generated by the DFA itself)
    //passed
    WRONG_PIN: 'wrong_pin',             //internal input (generated by the DFA itself)
    //failed
    SELECT_WITHDRAW: 'select_withdraw',
    SELECT_BALANCE: 'select_balance',
    ENTER_AMOUNT: 'enter_amount',       
    CONFIRM: 'confirm',
    BACK: 'back',                       //go back to transaction menu
    CANCEL: 'cancel',
    RESET: 'reset'
}

//define account database
//simple object, not integrating a real backend
const ACCOUNTS = {
    '12345': {
        pin: '1234',
        balance: 5000,
        name: 'Account 1'
    },
    '67890': {
        pin: '5678',
        balance: 5000,
        name: 'Account 2'
    },
}

//create the dfa class structure
//maintains:
    //the current state
    //internal data(PIN buffer, fail counter, balance)
    //transition history (for the log display)

class ATM_DFA {
    constructor() {
        //current state - starts at q0
        this.currentState = STATES.S0

        //input buffers
        this.accountBuffer = ''         //stores account digits as user types
        this.pinBuffer = ''             //stores PIN digits as user types
        this.amountBuffer = ''          //stores amount digits as user types

        //account management
        this.currentAccount = null      //reference to logged-in account
                                        //null since no one is logged in yet
        //security
        this.failedAttempts = 0
        this.maxAttempts = 3

        //transaction data
        this.transactionType = null;    //'withdraw' or 'balance'

        //history tracking (for UI display)
        this.transitionHistory = [];    //array of {from, input, to} objects
    }
}

//build the transition function (δ)
//given a current state and an input, return the next state (δ: Q × Σ → Q)
function transition(input) {

    const fromState = this.currentState     //save the starting state (for history)
    let toState = fromState                 //will hold the next state
                                            //default: stay in same state(no transition)
    let message = ''                        //will hold the screen message
    let action = null                       //will hold any side effects

    //Define δ for every (state, input) pair
    switch (this.currentState) {
        case STATES.S0:
            if (input === INPUTS.START) {
                toState = STATES.S1
                message = 'Enter your 5-digit account number.'
                this.accountBuffer = ''         //start fresh
                action = 'show_keypad'
            } else {
                message = 'Press START to begin transaction';
            }
            break;
        case STATES.S1:         //account entry(self-loop!)
            if (input === INPUTS.ENTER_DIGIT) {
                toState = STATES.S1;   //stay at s1 while collecting digits
                this.accountBuffer += '0'    //Placeholder (actual digit comes from UI)
                message = 'Account: ' + this.accountBuffer;
                
                if (this.pinBuffer.length === 5) {
                    action = 'auto_submit_account'
                }
            } else if (input === INPUTS.SUBMIT_ACCOUNT) {
                //validate account
                if (this.validateAccount()) {       //validateAccount function
                    toState = STATES.S2
                    this.currentAccount = ACCOUNTS[this.accountBuffer]
                    message = 'Account valid. Enter PIN.'
                    this.pinBuffer = ''     //prepare for PIN entry
                    action = 'start_pin_entry'
                } else {
                    //wrong account - just show error
                    //stay at S1 to allow entry
                    message = 'Invalid account number. Please try again.'
                    this.accountBuffer = ''         //clear for retry
                }
            } else if (input === INPUTS.CANCEL) {
                toState = STATES.S0
                message = 'Transaction cancelled.'
                this.accountBuffer = '';        //clear PIN buffer
                action = 'reset_atm'
            } else {
                message = 'Account: ' + this.accountBuffer
            }
            break
        case STATES.S3:
            if (input === INPUTS.SELECT_WITHDRAW) {
                toState = STATES.S4
                this.transactionType = 'withdraw'
                message =  `Withdraw $${this.withdrawAmount}?`
                action = 'show_confirm_button'
            } else if (input === INPUTS.SELECT_BALANCE) {
                toState = STATES.S4
                this.transactionType = 'balance'
            }
    }
}
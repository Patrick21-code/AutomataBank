/*
    DFA is defined as a 5-tuple: (Q, Σ, δ, q0, F)
    - Q = set of states
    - Σ = alphabet (input symbols)
    - δ = transition function
    - q0 = start state
    - F = set of accept states
    
    Version: 2.1 - Fixed S6 highlighting for account validation
*/

//define states (Q)
const STATES = {
    S0: 'idle',             //q0 - start state
    S1: 'account_entry',   
    S2: 'pin_entry',
    S3: 'authenticated',
    S4: 'amount_entry',
    S5: 'balance_display',
    S6: 'rejected',         //wrong credentials, can retry
    S7: 'done'              //accepting state
}

//define alphabet (Σ)
//all possible inputs that my DFA can receive
const INPUTS = {
    START: 'start',
    ENTER_DIGIT: 'enter_digit',
    BACKSPACE: 'backspace',             //delete last digit
    CLEAR: 'clear',                     //clear current input buffer
    SUBMIT_ACCOUNT: 'submit_account',
    CORRECT_ACCOUNT: 'correct_account', //internal input
    //passed
    WRONG_ACCOUNT: 'wrong_account',     //internal input
    //failed
    SUBMIT_PIN: 'submit_pin',           
    CORRECT_PIN: 'correct_pin',         //internal input (generated by the DFA itself)
    //passed
    WRONG_PIN: 'wrong_pin',             //internal input (generated by the DFA itself)
    //failed
    SELECT_WITHDRAW: 'select_withdraw',
    SELECT_BALANCE: 'select_balance',
    ENTER_AMOUNT: 'enter_amount',       
    CONFIRM: 'confirm',
    BACK: 'back',                       //go back to transaction menu
    CANCEL: 'cancel',
    RESET: 'reset'
}

//define account database
//simple object, not integrating a real backend
const ACCOUNTS = {
    '12345': {
        pin: '1234',
        balance: 5000,
        name: 'Account 1'
    },
    '67890': {
        pin: '5678',
        balance: 5000,
        name: 'Account 2'
    },
}

//create the dfa class structure
//maintains:
    //the current state
    //internal data(PIN buffer, fail counter, balance)
    //transition history (for the log display)

class ATM_DFA {
    constructor() {
        //current state - starts at q0
        this.currentState = STATES.S0

        //input buffers
        this.accountBuffer = ''         //stores account digits as user types
        this.pinBuffer = ''             //stores PIN digits as user types
        this.amountBuffer = ''          //stores amount digits as user types

        //account management
        this.currentAccount = null      //reference to logged-in account
                                        //null since no one is logged in yet
        //security
        this.pinFailedAttempts = 0
        this.maxPinAttempts = 3

        //transaction data
        this.transactionType = null;    //'withdraw' or 'balance'
        
        //rejection tracking
        this.rejectionReason = null;    //tracks why we're in S6

        //history tracking (for UI display)
        this.transitionHistory = [];    //array of {from, input, to} objects
    }

//build the transition function (δ)
//given a current state and an input, return the next state (δ: Q × Σ → Q)
transition(input) {

    const fromState = this.currentState     //save the starting state (for history)
    let toState = fromState                 //will hold the next state
                                            //default: stay in same state(no transition)
    let message = ''                        //will hold the screen message
    let action = null                       //will hold any side effects

    //Define δ for every (state, input) pair
    switch (this.currentState) {
        case STATES.S0:
            if (input === INPUTS.START) {
                toState = STATES.S1
                message = 'Enter your 5-digit account number.'
                this.accountBuffer = ''         //start fresh
                action = 'show_keypad'
            } else {
                message = 'Press START to begin transaction';
            }
            break;
        case STATES.S1:         //account entry(self-loop!)
            if (input === INPUTS.ENTER_DIGIT) {
                toState = STATES.S1;   //stay at s1 while collecting digits
                // Note: actual digit is added by UI handler before calling transition
                message = `Account: ${this.accountBuffer}`; //show in plain text
                
                if (this.accountBuffer.length === 5) {
                    action = 'auto_submit_account'
                }
            } else if (input === INPUTS.BACKSPACE) {
                toState = STATES.S1;
                this.accountBuffer = this.accountBuffer.slice(0, -1);
                message = `Account: ${this.accountBuffer}`;
            } else if (input === INPUTS.CLEAR) {
                toState = STATES.S1;
                this.accountBuffer = '';
                message = 'Account cleared. Enter your 5-digit account number.';
            } else if (input === INPUTS.SUBMIT_ACCOUNT) {
                //validate account length first
                if (this.accountBuffer.length < 5) {
                    toState = STATES.S6
                    message = 'Account number too short. Transaction rejected.'
                    this.accountBuffer = ''
                    this.rejectionReason = 'account_too_short'
                    action = 'show_retry_options'
                } else if (this.accountBuffer.length > 5) {
                    toState = STATES.S6
                    message = 'Account number too long. Transaction rejected.'
                    this.accountBuffer = ''
                    this.rejectionReason = 'account_too_long'
                    action = 'show_retry_options'
                } else if (this.validateAccount()) {       //validateAccount function
                    toState = STATES.S2
                    this.currentAccount = ACCOUNTS[this.accountBuffer]
                    message = 'Account valid. Enter PIN.'
                    this.pinBuffer = ''     //prepare for PIN entry
                    action = 'start_pin_entry'
                } else {
                    //invalid account - go to S6
                    toState = STATES.S6
                    message = 'Invalid account number. Transaction rejected.'
                    this.accountBuffer = ''
                    this.rejectionReason = 'account_invalid'
                    action = 'show_retry_options'
                }
            } else if (input === INPUTS.CANCEL) {
                toState = STATES.S0
                message = 'Transaction cancelled.'
                this.accountBuffer = '';        //clear PIN buffer
                action = 'reset_atm'
            } else {
                message = 'Account: ' + this.accountBuffer
            }
            break
        case STATES.S2:     //pin entry (self-loop)
            if (input === INPUTS.ENTER_DIGIT) {
                toState = STATES.S2     //stay at S2 while collecting digits
                // Note: actual digit is added by UI handler before calling transition
                message = 'PIN: ' + '•'.repeat(this.pinBuffer.length)

                if (this.pinBuffer.length === 4)
                    action = 'auto_submit_pin';

            } else if (input === INPUTS.BACKSPACE) {
                toState = STATES.S2;
                this.pinBuffer = this.pinBuffer.slice(0, -1);
                message = 'PIN: ' + '•'.repeat(this.pinBuffer.length);
            } else if (input === INPUTS.CLEAR) {
                toState = STATES.S2;
                this.pinBuffer = '';
                message = 'PIN cleared. Enter your 4-digit PIN.';
            } else if (input === INPUTS.SUBMIT_PIN) {
                //validate PIN length first
                if (this.pinBuffer.length < 4) {
                    toState = STATES.S6
                    message = 'PIN too short. Transaction rejected.'
                    this.pinBuffer = ''
                    this.rejectionReason = 'pin_invalid'
                    action = 'show_retry_options'
                } else if (this.pinBuffer.length > 4) {
                    toState = STATES.S6
                    message = 'PIN too long. Transaction rejected.'
                    this.pinBuffer = ''
                    this.rejectionReason = 'pin_invalid'
                    action = 'show_retry_options'
                } else if (this.validatePin()) {
                    toState = STATES.S3
                    message = 'PIN correct. Select transaction.'
                    action = 'show_transaction_menu'
                    this.pinFailedAttempts = 0;     //reset fail counter on success
                } else {
                    toState = STATES.S6;
                    this.pinFailedAttempts++
                    this.rejectionReason = 'pin_wrong'
        
                    if (this.pinFailedAttempts >= this.maxPinAttempts) {
                        message = 'Card blocked. Too many wrong PIN attempts'
                        action = 'block_card'
                    } else {
                        message = `WRONG PIN. ${this.maxPinAttempts - this.pinFailedAttempts} attempts remaining`
                        action = 'show_retry_options'
                    }
                }
                this.pinBuffer = ''     //clear pin buffer after submission
            } else if (input === INPUTS.CANCEL) {
                toState = STATES.S0
                message = 'Transaction cancelled.'
                this.pinBuffer = ''
                action = 'reset_atm'
            } else {
                message = 'PIN: ' + '•'.repeat(this.pinBuffer.length);
            }
            break
        case STATES.S3:     //authenticated - choose transaction
            if (input === INPUTS.SELECT_WITHDRAW) {
                toState = STATES.S4
                this.transactionType = 'withdraw'
                this.amountBuffer = ''
                message = 'Enter withdrawal amount.'
                action = 'show_amount_keypad'
            } else if (input === INPUTS.SELECT_BALANCE) {
                toState = STATES.S5
                this.transactionType = 'balance'
                message = `Your balance: $${this.getBalance()}`
                action = 'show_balance'
            } else if (input === INPUTS.CANCEL) {
                toState = STATES.S0
                message = 'Transaction cancelled'
                action = 'reset_atm'
            } else {
                message = 'Select transaction: Withdraw or Balance'
            }
            break
        
        case STATES.S4:         //amount entry (self-loop)
            if (input === INPUTS.ENTER_AMOUNT) {
                toState = STATES.S4     //self-loop: stay at S4 while collecting digits
                // Note: actual digit is added by UI handler before calling transition
                message = 'Amount: $' + this.amountBuffer
            } else if (input === INPUTS.BACKSPACE) {
                toState = STATES.S4;
                this.amountBuffer = this.amountBuffer.slice(0, -1);
                message = 'Amount: $' + this.amountBuffer;
            } else if (input === INPUTS.CLEAR) {
                toState = STATES.S4;
                this.amountBuffer = '';
                message = 'Amount cleared. Enter withdrawal amount.';
            } else if (input === INPUTS.CONFIRM) {
                const amount = parseInt(this.amountBuffer)
                
                //check if amount exceeds account balance
                if (amount > this.getBalance()) {
                    toState = STATES.S6
                    message = `Insufficient funds. Balance: $${this.getBalance()}. Transaction rejected.`
                    this.amountBuffer = ''
                    this.rejectionReason = 'insufficient_funds'
                    action = 'show_retry_options'
                } else {
                    const result = this.processWithdrawal(amount)
                    
                    if (result.success) {
                        toState = STATES.S7
                        message = `Withdrawal successful! New balance: $${result.newBalance}`
                        action = 'dispense_cash'
                    } else {
                        //stay at s4 if withdrawal fails for other reasons
                        message = `Error: ${result.error}. Try again.`
                        this.amountBuffer = ''          //reset
                    }
                }
            } else if (input === INPUTS.CANCEL) {
                toState = STATES.S0
                message = 'Transaction cancelled.'
                this.amountBuffer = ''
                action = 'reset_atm'
            } else if (input === INPUTS.BACK) {
                toState = STATES.S3
                message = 'Select transaction: Withdraw or Balance.'
                this.amountBuffer = ''
                action = 'show_transaction_menu'
            } else {
                message = 'Amount: $' + this.amountBuffer
            }
            break
        case STATES.S5:    //balance display
            if (input === INPUTS.BACK) {
                toState = STATES.S3     // back to transaction menu
                message = 'Select transaction: Withdraw or Balance.'
                action = 'show_transaction_menu'
            } else if (input === INPUTS.CONFIRM) {
                toState = STATES.S7     // done button goes to S7
                message = 'Transaction complete.'
                action = 'complete_transaction'
            } else if (input === INPUTS.CANCEL) {
                toState = STATES.S0
                message = 'Thank you for using our ATM'
                action = 'reset_atm'
            } else {
                message = `Your balance: $${this.getBalance()}`
            }
            break
        case STATES.S6:             //rejected state
            if (input === INPUTS.ENTER_DIGIT) {
                // Check rejection reason to determine where to go
                if (this.rejectionReason && this.rejectionReason.includes('account')) {
                    // Account-related rejection - go back to account entry
                    toState = STATES.S1
                    message = 'Enter account number.'
                    this.accountBuffer = ''
                    this.rejectionReason = null
                    action = 'show_keypad'
                } else if (this.rejectionReason === 'insufficient_funds') {
                    // Insufficient funds - go back to amount entry
                    toState = STATES.S4
                    message = 'Enter withdrawal amount.'
                    this.amountBuffer = ''
                    this.rejectionReason = null
                    action = 'show_amount_keypad'
                } else {
                    // PIN-related rejection - go back to PIN entry
                    toState = STATES.S2
                    message = 'Try your PIN again.'
                    this.pinBuffer = ''
                    this.rejectionReason = null
                    action = 'show_keypad'
                }
            } else if (input === INPUTS.CANCEL) {
                //cancel and return to idle
                toState = STATES.S0
                message = 'Transaction cancelled.'
                this.accountBuffer = ''
                this.pinBuffer = ''
                this.currentAccount = null
                this.rejectionReason = null
                this.pinFailedAttempts = 0
                action = 'reset_atm'
            } else if (input === INPUTS.RESET) {
                toState = STATES.S0
                message = 'Session ended'
                this.pinFailedAttempts = 0
                this.rejectionReason = null
                action = 'reset_atm'
            }
            break
        case STATES.S7: //done (accepting state) - transaction complete
            if (input === INPUTS.RESET) {
                toState = STATES.S0
                message = 'Thank you for using our ATM.'
                this.currentAccount = null
                this.pinFailedAttempts = 0
                action = 'reset_atm'
            } else {
                message = 'Transaction complete. Press RESET to finish.'
            }
            break
    }

    this.currentState = toState
    this.transitionHistory.push({from: fromState, input, to: toState})

    console.log(`🔄 DFA Transition: ${fromState} --[${input}]--> ${toState}`);
    if (this.rejectionReason) {
        console.log(`   Rejection reason: ${this.rejectionReason}`);
    }

    return {fromState, toState, message, action, currentBalance: this.getBalance()}
}

    //helper methods for the transition function

    validateAccount () {
        return ACCOUNTS.hasOwnProperty(this.accountBuffer)
    }

    validatePin () {
        if (!this.currentAccount) return false      //if not the account, return false
        return this.pinBuffer === this.currentAccount.pin
    }

    processWithdrawal(amount) {
        if (!this.currentAccount) {
            return {success: false, error: 'No account logged in'}
        }

        if (amount <= 0) {
            return {success: false, error: 'Invalid amount'}
        }

        if (this.currentAccount.balance < amount) {
            return {success: false, error: 'Insufficient funds'}
        }

        this.currentAccount.balance -= amount

        return {
            success: true,
            newBalance: this.currentAccount.balance
        }
    }

    getBalance() {
        return this.currentAccount ? this.currentAccount.balance : 0
        //the certain account has to much the current account balance
    }

    //return DFA to initial state
    reset() {
        this.currentState = STATES.S0;
        this.accountBuffer = ''
        this.pinBuffer = ''
        this.amountBuffer = ''
        this.currentAccount = null
        this.pinFailedAttempts = 0
        this.rejectionReason = null
        this.transitionHistory = []
    }

    getStateDescription () { 
        const descriptions = {
            [STATES.S0]: 'Idle - Waiting to start',
            [STATES.S1]: 'Account Entry - Collecting digits',
            [STATES.S2]: 'PIN Entry - Collecting digits',
            [STATES.S3]: 'Authenticated - Choose transaction',
            [STATES.S4]: 'Amount Entry - Enter withdrawal amount',
            [STATES.S5]: 'Balance Display - Showing balance',
            [STATES.S6]: 'Rejected - Wrong credentials',
            [STATES.S7]: 'Done - Transaction complete'
        }
        return descriptions[this.currentState] || 'Unknown state';
    }

    //get all transitions so far
    getTransitionHistory () {
        return this.transitionHistory
    }

    //check if we're at an accept state
    isInAcceptState() {
        return this.currentState === STATES.S7
    }
}
//export everything
//export {ATM_DFA, STATES, INPUTS}


window.ATM_DFA = ATM_DFA;
window.STATES = STATES;
window.INPUTS = INPUTS;

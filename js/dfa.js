//create the dfa class structure
//maintains:
    //the current state
    //internal data(PIN buffer, fail counter, balance)
    //transition history (for the log display)

class ATM_DFA {
    constructor() {
        //current state - starts at q0
        this.currentState = STATES.S0

        //PIN handling
        this.pinBuffer = ''             //stores digits as user types
        this.correctPin = '1234'        //the PIN (hardcoded)
        this.failedAttempts = 0         //Security counter
        this.maxAttempts = 3            //lock card after 3 wrong tries

        //transaction data
        this.transactionType = null;    //'withdraw' or 'balance'
        this.balance  = 5000;           // mock account balance
        this.withdrawAmount = 100       //fixed withdrawal for simplicity
        
        //history tracking
        this.transitionHistory = [];    //array of {from, input, to} objects
    }
}

//build the transition function (δ)
//given a current state and an input, return the next state (δ: Q × Σ → Q)

function transition(input) {

    const fromState = this.currentState     //save the starting state (for history)
    let toState = fromState                 //will hold the next state
                                            //default: stay in same state(no transition)
    let message = ''                        //will hold the screen message
    let action = null                       //will hold any side effects

    //Define δ for every (state, input) pair
    switch (this.currentState) {
        case STATES.S0:
            if (input === INPUTS.INSERT_CARD) {
                toState = STATES.S1
                message = 'Card accepted. Please enter your PIN.'
                action = 'show_keypad';
            } else {
                // invalid input for this state - stay at S0
                message = 'Please insert your card to begin';
            }
            break;
        case STATES.S1:
            if (input === INPUTS.ENTER_DIGIT) {
                toState = STATES.S2;
                message = 'PIN: •'
                this.pinBuffer = '' //start fresh PIN entry
                action = 'start_pin_entry'
            } else if (input === INPUTS.CANCEL) {
                toState = STATES.S0
                message = 'Transaction cancelled'
                action = 'eject_card'
            } else {
                message = 'Please enter your PIN'
            }
            break;
        case STATES.S2:
            if (input === INPUTS.ENTER_DIGIT) {
                toState = STATES.S2;                                    //SELF-LOOP: Stay at S2 while collecting digits
                this.pinBuffer += '0'                                   //Placeholder (actual digit comes from UI)
                message = 'PIN: ' + '•'.repeat(this.pinBuffer.length)
                
                if (this.pinBuffer.length === 4) {
                    action = 'auto_submit'
                }
            } else if (input === INPUTS.SUBMIT_PIN) {
                //if correct PIN, move to S3
                if (this.validatePin()) {
                    toState = STATES.S3
                    message = 'PIN correct. Select transaction: '
                    action = 'show_transaction_menu'
                    this.failedAttemps = 0;         //reset fail counter on success
                } else {
        
                    toState = STATES.S5             //Wrong PIN - move to rejected state        
                    this.failedAttempts++           
                    
                    if (this.failedAttemps >= this.maxAttempts) {
                        message = 'Card blocked. Too many failed attempts.'
                        action = 'block_card'
                    } else {
                        message =  `Wrong PIN. ${this.maxAttempts - this.failedAttemps} attempts remaining`;
                        action = 'show_retry_options'
                    }
                }

                this.pinBuffer = ''         //clear PIN buffer after submission
            } else if (input === INPUTS.CANCEL) {
                toState = STATES.S0
                message = 'Transaction cancelled.'
                this.pinBuffer = '';        //clear PIN buffer
                action = 'eject_card'
            } else {
                message = 'PIN: ' + '•'.repeat(this.pinBuffer.length)
            }
            break
        case STATES.S3:
            if (input === INPUTS.SELECT_WITHDRAW) {
                toState = STATES.S4
                this.transactionType = 'withdraw'
                message =  `Withdraw $${this.withdrawAmount}?`
                action = 'show_confirm_button'
            } else if (input === INPUTS.SELECT_BALANCE) {
                toState = STATES.S4
                this.transactionType = 'balance'
            }
    }
}